export function ar(text: string): string {
  return phrases[text] || text;
}

function translateHtml(html: string, pairs: Array<readonly [string, string]>): string {
  const translate = (chunk: string) => pairs.reduce((output, [source, target]) => output.split(source).join(target), chunk);
  return html
    .split(/(<script[\s\S]*?<\/script>|<style[\s\S]*?<\/style>)/gi)
    .map((chunk) => {
      if (/^<(script|style)\b/i.test(chunk)) return chunk;
      return chunk
        .split(/(<[^>]+>)/g)
        .map((part) => (part.startsWith("<") ? part : translate(part)))
        .join("");
    })
    .join("");
}

const EN_TO_AR = Object.entries(phrases).sort((a, b) => b[0].length - a[0].length) as Array<readonly [string, string]>;
const AR_TO_EN = Object.entries(phrases)
  .map(([en, target]) => [target, en] as const)
  .sort((a, b) => b[0].length - a[0].length);

export function localizeHtml(html: string): string {
  return translateHtml(html, EN_TO_AR);
}

/** Reverse of localizeHtml: turns the Arabic-localized chrome back into English (text nodes only). */
export function enFromAr(html: string): string {
  return translateHtml(html, AR_TO_EN);
}
